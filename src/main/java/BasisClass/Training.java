package BasisClass;

public class Training {

    private int trainingId;
    private int bufferId;
    private double trainingHours;
    private String trainingDescryption;

    public int getTrainingId() {
        return trainingId;
    }

    public void setTrainingId(int trainingId) {
        this.trainingId = trainingId;
    }

    public int getBufferId() {
        return bufferId;
    }

    public void setBufferId(int bufferId) {
        this.bufferId = bufferId;
    }

    public double getTrainingHours() {
        return trainingHours;
    }

    public void setTrainingHours(double trainingHours) {
        this.trainingHours = trainingHours;
    }

    public String getTrainingDescryption() {
        return trainingDescryption;
    }

    public void setTrainingDescryption(String trainingDescryption) {
        this.trainingDescryption = trainingDescryption;
    }
}
